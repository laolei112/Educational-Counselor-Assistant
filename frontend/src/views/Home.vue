<script setup lang="ts">
import { onMounted, onUnmounted, computed, ref } from 'vue'
import { storeToRefs } from 'pinia'
import { useSchoolStore } from '@/stores/school'
import { useLanguageStore } from '@/stores/language'
import SchoolCard from '@/components/SchoolCard.vue'
import SchoolDetailModal from '@/components/SchoolDetailModal.vue'
import LanguageSwitcher from '@/components/LanguageSwitcher.vue'
import type { School } from '@/types/school'

const schoolStore = useSchoolStore()
const languageStore = useLanguageStore()

// 获取多语言文本
const getText = (key: string) => {
  return languageStore.getText(key)
}
const { 
  currentType, 
  filteredSchools, 
  stats, 
  isLoading, 
  hasError, 
  error,
  enableMock,
  pagination,
  searchKeyword,
  hasSearchResults,
  currentPageData,
  hasMoreData,
  isLoadingMore,
  filters,
  filterOptions
} = storeToRefs(schoolStore)
const { 
  setSchoolType, 
  fetchSchools, 
  clearError, 
  searchSchools, 
  clearSearch, 
  loadMore,
  setFilters,
  clearFilters,
  initFilters
} = schoolStore

// 移除本地状态，使用store中的固定页面大小

// 移除分页相关计算属性，使用无限滚动

// 学校详情弹窗相关
const selectedSchool = ref<School | null>(null)
const showDetailModal = ref(false)

// 滚动加载相关
let isLoadingMoreData = false

// 滚动检测函数
const handleScroll = async () => {
  if (isLoadingMoreData || !hasMoreData.value) return
  
  const scrollTop = window.pageYOffset || document.documentElement.scrollTop
  const windowHeight = window.innerHeight
  const documentHeight = document.documentElement.scrollHeight
  
  // 当滚动到距离底部100px时触发加载
  if (scrollTop + windowHeight >= documentHeight - 100) {
    isLoadingMoreData = true
    try {
      await loadMore()
    } finally {
      isLoadingMoreData = false
    }
  }
}

// 活动中的下拉菜单
const activeFilterDropdown = ref<string | null>(null)

// 是否有活动的筛选条件
const hasActiveFilters = computed(() => {
  return filters.value.district !== '' || 
         filters.value.schoolNet !== '' || 
         filters.value.category !== '' ||
         filters.value.banding !== ''
})

// 切换筛选下拉菜单
const toggleFilterDropdown = (type: string) => {
  if (activeFilterDropdown.value === type) {
    activeFilterDropdown.value = null
  } else {
    activeFilterDropdown.value = type
  }
}

// 选择筛选选项
const selectFilter = async (type: keyof typeof filters.value, value: string) => {
  if (type === 'district') {
    filters.value.district = value
  } else if (type === 'schoolNet') {
    filters.value.schoolNet = value
  } else if (type === 'category') {
    filters.value.category = value
  } else if (type === 'banding') {
    filters.value.banding = value
  }
  
  activeFilterDropdown.value = null
  await handleFilterChange()
}

// 处理筛选条件变化
const handleFilterChange = async () => {
  await setFilters({
    district: filters.value.district,
    schoolNet: filters.value.schoolNet,
    category: filters.value.category,
    banding: filters.value.banding
  })
}

// 处理清除筛选
const handleClearFilters = async () => {
  await clearFilters()
}

// 点击外部关闭下拉菜单
const handleClickOutside = (event: Event) => {
  const target = event.target as HTMLElement
  if (!target.closest('.filters-section')) {
    activeFilterDropdown.value = null
  }
}

// 组件挂载时获取数据并添加滚动监听
onMounted(async () => {
  // 初始化语言设置
  languageStore.initLanguage()
  
  // 初始化筛选选项
  await initFilters()
  
  await fetchSchools()
  window.addEventListener('scroll', handleScroll)
  document.addEventListener('click', handleClickOutside)
})

// 组件卸载时移除滚动监听
onUnmounted(() => {
  window.removeEventListener('scroll', handleScroll)
  document.removeEventListener('click', handleClickOutside)
  // 关闭下拉菜单
  activeFilterDropdown.value = null
})

// 处理学校类型切换
const handleTypeChange = async (type: 'primary' | 'secondary') => {
  await setSchoolType(type)
}

// 处理学校卡片点击
const handleSchoolClick = (school: School) => {
  selectedSchool.value = school
  showDetailModal.value = true
}

// 处理关闭弹窗
const handleCloseModal = () => {
  showDetailModal.value = false
  setTimeout(() => {
    selectedSchool.value = null
  }, 300) // 延迟清空，让动画完成
}

// 处理实时搜索输入
let searchTimeout: ReturnType<typeof setTimeout> | null = null
const handleSearchInput = () => {
  // 清除之前的定时器
  if (searchTimeout) {
    clearTimeout(searchTimeout)
  }
  
  // 设置新的定时器，延迟800ms执行搜索（增加延迟，避免频繁搜索）
  searchTimeout = setTimeout(async () => {
    if (searchKeyword.value.trim()) {
      await searchSchools(searchKeyword.value.trim())
    } else {
      await clearSearch()
    }
  }, 800)
}

// 处理搜索框获得焦点
const handleSearchFocus = () => {
  // 可以在这里添加一些焦点状态的逻辑
}

// 处理搜索框失去焦点
const handleSearchBlur = () => {
  // 可以在这里添加一些失去焦点状态的逻辑
}

// 处理清空搜索
const handleClearSearch = async () => {
  // 清除定时器
  if (searchTimeout) {
    clearTimeout(searchTimeout)
    searchTimeout = null
  }
  searchKeyword.value = ''
  await clearSearch()
}

// 移除手动加载更多方法，改为自动滚动加载

// 移除页面大小变化处理，使用固定页面大小

// 重新加载数据
const handleRetry = async () => {
  clearError()
  await fetchSchools()
}
</script>

